  }

  static void showQuizResult(BuildContext context, int obtained, int total) {
    final percentage = (obtained / total * 100).round();
    final isPassed = percentage >= 50;

    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Row(
          children: [
            Icon(
              isPassed ? Icons.emoji_events : Icons.refresh,
              color: Colors.white,
            ),
            const SizedBox(width: 12),
            Expanded(
              child: Text(
                isPassed
                    ? 'Great job! You scored $obtained/$total ($percentage%)'
                    : 'You scored $obtained/$total ($percentage%). Keep practicing!',
                style: const TextStyle(
                  color: Colors.white,
                  fontSize: 14,
                  fontWeight: FontWeight.w600,
                ),
              ),
            ),
          ],
        ),
        backgroundColor:
            isPassed ? const Color(0xFF4CAF50) : const Color(0xFFFF9800),
        behavior: SnackBarBehavior.floating,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
        margin: const EdgeInsets.all(16),
        duration: const Duration(seconds: 4),
        elevation: 6,
      ),
    );
  }

